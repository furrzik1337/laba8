def find_elements_before_last_pair_of_evens(tup):
    # Проходим по кортежу и ищем последнюю пару соседних четных чисел
    for i in range(len(tup) - 1):
        if tup[i] % 2 == 0 and tup[i + 1] % 2 == 0:
            # Если нашли пару, выводим все элементы до нее включительно
            print("Элементы, предшествующие последней паре соседних четных чисел:", tup[:i + 1])
            return
    # Если не нашли ни одной пары, сообщаем об этом
    print("Нет пар соседних четных чисел.")

# Пример использования
tup = (1, 2, 4, 5, 6, 8, 10)
find_elements_before_last_pair_of_evens(tup)
